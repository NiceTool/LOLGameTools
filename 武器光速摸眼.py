def down(event):
    # 10 (Q), 11 (W), 12 (E), 13 (R)
    key = event.Key
    if key == "2" or key == "4":
        threading.Thread(target=check_w).start()
    return True


def check_w():
    time.sleep(0.05)
    sendkey(0x10, 1)
    sendkey(0x10, 0)


def action():
    hm = PyHook3.HookManager()
    hm.KeyDown = down
    hm.HookKeyboard()
    pythoncom.PumpMessages()


print('武器大师光速摸眼启动')
print('2，4，插眼自动Q摸眼')
action()
